var course = document.getElementById('course').options;
var semester = document.getElementById('semester').options;
var subjects = document.getElementById('subject').options;
document.getElementById('a').style.display = 'none';

document.getElementById('submit').addEventListener('click',()=>{
    var courseIndex = document.getElementById('course').selectedIndex;
    var semesterIndex = document.getElementById('semester').selectedIndex;
    var subjectIndex = document.getElementById('subject').selectedIndex;
    var i1 = course[courseIndex].value;
    var i2 = semester[semesterIndex].value;
    var i3 = subjects[subjectIndex].value;
    link = "https://drive.google.com/file/d/" + linkObject[i1][i2][i3] +"/view?usp=sharing" ;
    var err = false;
    if(courseIndex == 0){
        document.getElementById('a').style.display = 'none';
        document.getElementById('h1').style.display = 'block';
        document.getElementById('h1').innerHTML = "Please select Course";
        err = true;
    }
    else if(semesterIndex == 0){
        document.getElementById('a').style.display = 'none';
        document.getElementById('h1').style.display = 'block';
        document.getElementById('h1').innerHTML = "Please select Semester";
        err = true;
    }
    else if(subjectIndex == 0){
        document.getElementById('a').style.display = 'none';
        document.getElementById('h1').style.display = 'block';
        document.getElementById('h1').innerHTML = "Please select subject";
        err = true;
    }
    if(err == false){
        document.getElementById('h1').style.display = 'none';
        document.getElementById('a').style.display = 'inline';
        document.getElementById('a').setAttribute('href',link);
    }
    
});
